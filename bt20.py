a = int(input("nhập số nguyên a bất kỳ: "))
b = int(input("nhập số nguyên b bất kỳ: "))

tong = a + b
tich = a * b
hieu = a - b
thuong = a / b
luy_thua = a^b
phan_nguyen = a // b
phan_du = a % b

print("tổng= ", tong)
print("tích= ", tich)
print("hiệu= ",hieu)
print("thương= ", thuong)
print("lũy thừa= ", luy_thua)
print("phần nguyên= ", phan_nguyen)
print("phần dư= ", phan_du)